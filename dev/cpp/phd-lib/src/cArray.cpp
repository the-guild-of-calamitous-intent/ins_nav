#include "cMathlib.h"


static char errMsg[ERROR_STRING_SIZE];


/////////////////////////////////////////////////////////////////
/////////////////////////////////////////////////////////////////
cArray::cArray(int s){
    type = ML_ARRAY;
    key = 0;
    size = s;

    array = NULL;
    array = new int[size];

    if(array == NULL){
        throw cMLError(cMLError::FATAL,"could not create array");
    }
}

/////////////////////////////////////////////////////////////////
/////////////////////////////////////////////////////////////////
cArray::~cArray(void){
    delete[] array;
    size = 0;
}
